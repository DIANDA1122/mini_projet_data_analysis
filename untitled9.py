"""*   GloVe

GloVe est un algorithme d'apprentissage non supervisé permettant d'obtenir des représentations vectorielles de mots. Il est entraîné à partir de statistiques globales de cooccurrence mot-mot issues d'un corpus. Les vecteurs sont appris en optimisant une fonction objectif qui vise à minimiser la différence entre le produit scalaire de deux vecteurs de mots et le logarithme de leur probabilité de cooccurrence.
"""

import gensim.downloader as api
import numpy as np

# Charger un modèle GloVe pré-entraîné (par exemple, 'glove-twitter-25' pour des vecteurs à 25 dimensions entraînés sur des données Twitter)
# Le téléchargement peut prendre un instant lors de la première utilisation.
