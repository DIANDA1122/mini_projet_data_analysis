"""*   BERT

### Régénération des Embeddings BERT (Correction de NameError)

Il semble que les embeddings BERT (`X_bert`) n'étaient pas disponibles lors de l'exécution précédente, entraînant une `NameError`. Pour corriger cela et s'assurer que les comparaisons de modèles soient complètes, nous allons régénérer les embeddings BERT avant de procéder à l'entraînement et à l'évaluation du modèle basé sur BERT.
"""

from transformers import AutoTokenizer, AutoModel
import torch
import numpy as np
import pandas as pd

# Load a pre-trained BERT tokenizer and model
# 'bert-base-uncased' is a commonly used model
print("Loading BERT tokenizer and model (bert-base-uncased)...")
tokenizer = AutoTokenizer.from_pretrained('bert-base-uncased')
model = AutoModel.from_pretrained('bert-base-uncased')
print("BERT tokenizer and model loaded.")

# Function to generate BERT embeddings for a list of texts
def get_bert_embeddings(texts, tokenizer, model, max_length=128):
    model.eval() # Set model to evaluation mode
    all_embeddings = []
    # Process texts in batches to reduce memory usage and speed up computation
    batch_size = 32 # You can adjust this batch size based on your available memory
    for i in range(0, len(texts), batch_size):
        batch_texts = texts[i:i + batch_size]

        # Tokenize the batch of texts and convert to input IDs
        encoded_input = tokenizer(
            batch_texts,
            padding='max_length',
            truncation=True,
            max_length=max_length,
            return_tensors='pt' # Return PyTorch tensors
        )

        # Move tensors to the same device as the model (e.g., CPU or GPU if available)
        device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        encoded_input = {k: v.to(device) for k, v in encoded_input.items()}
        model.to(device)

        with torch.no_grad(): # Disable gradient calculation for inference
            model_output = model(**encoded_input)

        # Get the embeddings of the [CLS] token (first token) as sentence embedding
        # You can also average the last_hidden_state output for all tokens
        sentence_embeddings = model_output.last_hidden_state[:, 0, :].cpu().numpy()
        all_embeddings.extend(sentence_embeddings)

    return np.array(all_embeddings)

