print("GloVe model loaded.")

# Fonction pour obtenir un vecteur de document en faisant la moyenne des vecteurs de motss
def get_glove_document_vector(words, model, vector_size):
    vector = np.zeros(vector_size)
    count = 0
    for word in words:
        if word in model:
            vector += model[word]
            count += 1
    if count > 0:
        vector /= count
    return vector

# Définir la taille du vecteur en fonction du modèle GloVe chargé
