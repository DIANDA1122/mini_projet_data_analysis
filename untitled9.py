"""###Prétraitement du texte

Mettre en œuvre plusieurs opérations de nettoyage :

*   Conversion en minuscules


"""

df['message'] = df['message'].str.lower()
display(df.head())

"""*   Suppression des caractères spéciaux.


"""

import re
df['message'] = df['message'].apply(lambda x: re.sub(r'[^a-zA-Z0-9\s.,!?]', '', x))
display(df.head())

"""*   Suppression des URLs"""

df['message'] = df['message'].apply(lambda x: re.sub(r'http\S+|www\S+', '', x))
display(df.head())

"""*   Suppression des stop words


"""

import nltk
from nltk.corpus import stopwords
nltk.download('stopwords')

stop_words = set(stopwords.words('french'))

def remove_stopwords(text):
    return ' '.join([word for word in str(text).split() if word not in stop_words])

df['message'] = df['message'].apply(remove_stopwords)
display(df.head())

"""*   Lemmatisation ou stemming (optionnel).

    Pour notre cas nous avons choisi la Lemmatisation
"""

import nltk
nltk.download('punkt')
nltk.download('wordnet')

from nltk.stem import WordNetLemmatizer

lemmatizer = WordNetLemmatizer()

def lemmatize_text(text):
    return ' '.join([lemmatizer.lemmatize(word) for word in str(text).split()])

df['message_lemmatized'] = df['message'].apply(lemmatize_text)
display(df[['message', 'message_lemmatized']].head())

